func CreateClassroom_invitations_createTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_v1_invitations",
		mcp.WithDescription("Creates an invitation. Only one invitation for a user and course may exist at a time. Delete and re-create an invitation to make changes. This method returns the following error codes: * `PERMISSION_DENIED` if the requesting user is not permitted to create invitations for this course or for access errors. * `NOT_FOUND` if the course or the user does not exist. * `FAILED_PRECONDITION`: * if the requested user's account is disabled. * if the user already has this role or a role with greater permissions. * for the following request errors: * IneligibleOwner * `ALREADY_EXISTS` if an invitation for the specified user and course already exists."),
		mcp.WithString("id", mcp.Description("Input parameter: Identifier assigned by Classroom. Read-only.")),
		mcp.WithString("role", mcp.Description("Input parameter: Role to invite the user to have. Must not be `COURSE_ROLE_UNSPECIFIED`.")),
		mcp.WithString("userId", mcp.Description("Input parameter: Identifier of the invited user. When specified as a parameter of a request, this identifier can be set to one of the following: * the numeric identifier for the user * the email address of the user * the string literal `\"me\"`, indicating the requesting user")),
		mcp.WithString("courseId", mcp.Description("Input parameter: Identifier of the course to invite the user to.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Classroom_invitations_createHandler(cfg),
	}
}
